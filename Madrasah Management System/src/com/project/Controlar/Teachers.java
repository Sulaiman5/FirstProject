    void allValue() {
        id = Integer.parseInt(txtSearch.getText());
        name = txtName.getText();
        father = txtFather.getText();
        religion = combReligion.getSelectedItem().toString();
        for (int i = 0; i < educationPanel.getComponentCount(); i++) {
            JCheckBox jcheck = (JCheckBox) educationPanel.getComponent(i);
            if (jcheck.isSelected()) {
                Education += jcheck.getText();
                Education += gap;
            }
        }
        if (male.isSelected()) {
            gender = "Male";
        } else if (female.isSelected()) {
            gender = "Female";
        } else {
            gender = "Others";
        }
        post = combPost.getSelectedItem().toString();
        present = txtPresent.getText();
        mobile = txtMobile.getText();
        mother = txtMother.getText();
        if (single.isSelected()) {
            marid = "Single";
        } else {
            marid = "Married";
        }
        subject = CombSubject.getSelectedItem().toString();
        NID = Integer.parseInt(txtNID.getText());
        salary = Double.parseDouble(txtSalary.getText());
        permanent = txtPermanent.getText();
    }

