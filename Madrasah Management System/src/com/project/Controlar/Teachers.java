    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtSearch.getText());
        Teacher val = new TeacherService().addsearch(id);
        txtName.setText(val.getName());
        txtFather.setText(val.getFather());
        combReligion.setSelectedItem(val.getReligion());
        if (educationPanel.isShowing()) {
            jCheckBox1.isSelected();
            jCheckBox2.isSelected();
            jCheckBox3.isSelected();
            jCheckBox4.isSelected();
        }
        if (val.getGender().equals("Male")) {
            male.setSelected(true);
        } else if (val.getGender().equals("Female")) {
            female.setSelected(true);
        } else {
            others.setSelected(true);
        }
        combPost.setSelectedItem(val.getPost());
        txtPresent.setText(val.getPresent());
        txtMobile.setText(val.getMobile());
        txtMother.setText(val.getMother());
        if (val.getMarid().equals("Single")) {
            single.setSelected(true);
        } else {
            married.setSelected(true);
        }
        CombSubject.setSelectedItem(val.getSubject());
        txtNID.setText(String.valueOf(val.getNID()));
        txtSalary.setText(String.valueOf(val.getSalary()));
        txtPermanent.setText(val.getPermanent());
    }//GEN-LAST:event_btnSearchActionPerformed

